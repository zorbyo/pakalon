export interface InitOptions {
  dir?: string;
  name?: string;
  initialPrompt?: string;
}

export async function cmdInit(
  cwdOrOptions: string | InitOptions = process.cwd(),
  initialPromptArg?: string,
): Promise<void> {
  const options = typeof cwdOrOptions === "string" ? undefined : cwdOrOptions;
  const cwd = typeof cwdOrOptions === "string"
    ? cwdOrOptions
    : (options?.dir ?? process.cwd());
  const initialPrompt = options?.initialPrompt ?? initialPromptArg;
  const pakalonDir = path.join(cwd, ".pakalon");

  if (fs.existsSync(pakalonDir)) {
    console.log(`[!] .pakalon/ already exists in ${cwd}`);
    console.log("Delete it first or use a different directory.");
    return;
  }

  console.log(`\n* Initializing Pakalon in ${cwd}...\n`);

  fs.mkdirSync(pakalonDir, { recursive: true });
  fs.mkdirSync(path.join(pakalonDir, "agents"), { recursive: true });

  const useAI = Boolean(
    initialPrompt ||
    process.env.PAKALON_TOKEN ||
    process.env.OPENROUTER_API_KEY ||
    process.env.PAKALON_USE_PROXY === "1",
  );

  const model = process.env.PAKALON_INIT_MODEL ?? "arcee-ai/trinity-large-preview:free";
  const authToken = process.env.PAKALON_TOKEN;
  const projectDescription = initialPrompt ?? options?.name ?? path.basename(cwd);

  if (useAI) {
    console.log(`  Using AI to generate planning files (model: ${model})...\n`);
  }

  for (const file of INIT_FILES) {
    const filePath = path.join(pakalonDir, file);
    const dir = path.dirname(filePath);
    fs.mkdirSync(dir, { recursive: true });

    let content: string;
    // security-policy.yml is a schema-specific YAML template — always use stub
    if (file === 'security-policy.yml') {
      content = SECURITY_POLICY_STUB;
      console.log(`  [OK] Created .pakalon/${file}`);
    } else if (useAI) {
      process.stdout.write(`   Generating .pakalon/${file}...`);
      content = await generateFileContent(file, projectDescription, model, authToken);
      process.stdout.write(" [OK]\n");
    } else {
      content = STUBS[file] ?? `# ${file}\n\n_Generated by pakalon init_\n`;
      console.log(`  [OK] Created .pakalon/${file}`);
    }

    fs.writeFileSync(filePath, content, "utf-8");
  }

  console.log("\n[OK] .pakalon/ initialized successfully!");
  if (!useAI) {
    console.log("\nTip: Pass a project description to get AI-generated planning files:");
    console.log('  pakalon /init "E-commerce platform with React + Node.js"');
  }

  // Scaffold project config if none exists
  const cfgPath = scaffoldProjectConfig(cwd);
  if (cfgPath) {
    console.log(`  [OK] Created ${cfgPath.replace(cwd + path.sep, "")}`);
  }

  console.log("\nNext steps:");
  console.log("  1. Review .pakalon/plan.md and refine as needed");
  console.log("  2. Review .pakalon/security-policy.yml and adjust security thresholds");
  console.log("  3. Run `pakalon /agents` to configure specialist agents");
  console.log("  4. Start chatting — Pakalon uses these files as context");

  // Pre-populate ~/.config/pakalon/agents.json with default specialist agents
  // only if the file does not yet exist (never overwrite user customisations)
  const agentsConfigFile = path.join(
    process.env["HOME"] ?? process.env["USERPROFILE"] ?? "~",
    ".config",
    "pakalon",
    "agents.json",
  );
  if (!fs.existsSync(agentsConfigFile)) {
    const now = new Date().toISOString();
    const defaultAgents = [
      {
        id: "agent-frontend",
        name: "frontend",
        description: "React / Next.js / Tailwind UI specialist",
        systemPrompt:
          "You are an expert frontend engineer. Focus on React, Next.js, Tailwind CSS, and shadcn/ui. " +
          "Write accessible, responsive, and performant UI code. Prefer server components where possible.",
        color: "cyan",
        allowedTools: ["read_file", "write_file", "edit_file", "multi_edit_files", "list_dir", "bash", "glob_find", "grep_search"],
        createdAt: now,
      },
      {
        id: "agent-backend",
        name: "backend",
        description: "API / database / server-side specialist",
        systemPrompt:
          "You are an expert backend engineer. Focus on FastAPI, Node.js, PostgreSQL, Redis, and REST API design. " +
          "Write clean, typed, and well-tested server-side code with proper error handling.",
        color: "green",
        allowedTools: ["read_file", "write_file", "edit_file", "multi_edit_files", "list_dir", "bash", "glob_find", "grep_search"],
        createdAt: now,
      },
      {
        id: "agent-testing",
        name: "testing",
        description: "Test coverage and quality assurance specialist",
        systemPrompt:
          "You are an expert QA engineer. Write comprehensive unit, integration, and e2e tests. " +
          "Use Vitest for TypeScript, pytest for Python, and Playwright for browser automation.",
        color: "yellow",
        allowedTools: ["read_file", "write_file", "edit_file", "bash", "glob_find", "grep_search"],
        createdAt: now,
      },
      {
        id: "agent-security",
        name: "security",
        description: "Security audit and hardening specialist",
        systemPrompt:
          "You are an expert security engineer. Audit code for OWASP Top 10 vulnerabilities, insecure dependencies, " +
          "and improper secret handling. Recommend and implement hardening measures.",
        color: "red",
        allowedTools: ["read_file", "list_dir", "bash", "glob_find", "grep_search"],
        createdAt: now,
      },
      {
        id: "agent-docs",
        name: "docs",
        description: "Documentation and technical writing specialist",
        systemPrompt:
          "You are an expert technical writer. Write clear, concise, and accurate documentation. " +
          "Generate READMEs, API docs, changelogs, and inline code comments.",
        color: "blue",
        allowedTools: ["read_file", "write_file", "edit_file", "list_dir", "glob_find", "grep_search"],
        createdAt: now,
      },
    ];
    try {
      fs.mkdirSync(path.dirname(agentsConfigFile), { recursive: true });
      fs.writeFileSync(agentsConfigFile, JSON.stringify(defaultAgents, null, 2), "utf-8");
      console.log("  [OK] Pre-populated default specialist agents (~/.config/pakalon/agents.json)");
    } catch (agentErr) {
      debugLog(`[init] Could not write default agents: ${agentErr}`);
    }
  }
  debugLog(`[init] Initialized .pakalon/ in ${cwd} (AI=${useAI})`);
}

