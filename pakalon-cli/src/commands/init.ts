type GenTarget =
  | "plan.md"
  | "task.md"
  | "user-stories.md"
  | "context-management.md"
  | "agents/skills.md";

function buildPrompt(target: GenTarget, projectDescription: string): string {
  const prompts: Record<GenTarget, string> = {
    "plan.md": `You are a senior software architect. Generate a detailed project plan in Markdown for the following project.

Project description: ${projectDescription}

The plan MUST include:
1. ## Overview — 2–3 sentence summary
2. ## Goals — bulleted list of 5–8 specific, measurable goals
3. ## Architecture — tech stack, key components, data flow
4. ## Phases — 3–5 named phases with deliverables
5. ## Timeline — realistic milestones with estimated durations
6. ## Risks — top 3 risks and mitigations

Be specific and actionable. Output ONLY the Markdown document.`,

    "task.md": `You are a senior engineering lead. Create a comprehensive task breakdown in Markdown for the following project.

Project description: ${projectDescription}

Requirements:
- Group tasks into phases (Phase 1: Setup, Phase 2: Core, Phase 3: Testing, Phase 4: Launch)
- Each task: checkbox, short title, optional 1-line note
- Include at least 20 concrete tasks total
- Cover: infrastructure, backend, frontend, testing, CI/CD, docs, security

Output ONLY the Markdown document.`,

    "user-stories.md": `You are a product manager. Write detailed user stories in Markdown for the following project.

Project description: ${projectDescription}

Requirements:
- Write 8–12 user stories
- Format: ## US{N} — {Title}\\n**As a** {role},\\n**I want to** {action},\\n**So that** {benefit}
- Include Acceptance Criteria (3–5 checkboxes each)
- Cover primary user journeys, admin flows, error cases

Output ONLY the Markdown document.`,

    "context-management.md": `You are an AI systems engineer. Create a context management strategy in Markdown for an AI coding assistant working on the following project.

Project description: ${projectDescription}

Include:
1. ## Token Budget — window size, phase allocations, rolling window strategy
2. ## Key Files — priority list for context loading
3. ## Memory Strategy — what to keep in short-term vs long-term memory
4. ## Compression Rules — when to summarize older context
5. ## Tool Budget — max parallel tool calls, retry policy

Output ONLY the Markdown document.`,

    "agents/skills.md": `You are an AI orchestration architect. Define specialist agent skills in Markdown for the following project.

Project description: ${projectDescription}

Include:
1. ## Available Skills — list skills relevant to this specific project (6–10 skills)
2. For each skill: name, purpose, input/output, when to invoke
3. ## Recommended Activation Order — phase-by-phase skill activation chart
4. ## Collaboration Patterns — how agents hand off work to each other

Output ONLY the Markdown document.`,
  };
  return prompts[target] ?? `Generate a detailed ${target} for: ${projectDescription}`;
}

// ---------------------------------------------------------------------------
// LLM generation with fallback
// ---------------------------------------------------------------------------

async function generateFileContent(
  target: string,
  projectDescription: string,
  model: string,
  authToken?: string,
): Promise<string> {
  const prompt = buildPrompt(target as GenTarget, projectDescription);
  try {
    const result = await generateCompletion({
      model,
      messages: [{ role: "user", content: prompt }],
      maxTokens: 2048,
      temperature: 0.7,
      authToken,
      // proxy mode auto-detected if no local key / PAKALON_USE_PROXY=1
    });
    return result.text.trim();
  } catch (err) {
    debugLog(`[init] LLM generation failed for ${target}: ${err}`);
    return STUBS[target] ?? `# ${target}\n\n_Generated by pakalon init_\n`;
  }
}

