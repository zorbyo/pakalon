/**
 * Load custom instructions from .pakalon/CLAUDE.md and related files.
 */
export function loadCustomInstructions(projectDir?: string): string {
  const dir = projectDir ?? process.cwd();
  const candidates = [
    path.join(dir, ".pakalon", "CLAUDE.md"),
    path.join(dir, ".pakalon", "instructions.md"),
    path.join(dir, ".pakalon", "plan.md"),
  ];

  const parts: string[] = [];

  for (const filePath of candidates) {
    try {
      if (fs.existsSync(filePath)) {
        const content = fs.readFileSync(filePath, "utf-8").trim();
        if (content) {
          parts.push(`### From ${path.basename(filePath)}`);
          parts.push("");
          parts.push(content);
          parts.push("");
        }
      }
    } catch {
      // Skip unreadable files
    }
  }

  return parts.join("\n");
}

/**
 * Quick builder function for common use cases.
 */
export function buildSystemPrompt(context: SystemPromptContext): string {
  return new SystemPromptBuilder()
    .setContext(context)
    .build();
}
